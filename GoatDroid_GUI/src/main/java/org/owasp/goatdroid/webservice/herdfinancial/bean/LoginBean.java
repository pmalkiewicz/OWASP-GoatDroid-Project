package org.owasp.goatdroid.webservice.herdfinancial.bean;

import java.util.HashMap;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlAccessorType;

@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class LoginBean extends BaseBean {

	int sessionToken;
	String userName;
	String accountNumber;
	HashMap<String, String> secretQuestions;

	public int getSessionToken() {
		return sessionToken;
	}

	public void setSessionToken(int sessionToken) {
		this.sessionToken = sessionToken;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getAccountNumber() {
		return accountNumber;
	}

	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}

	public HashMap<String, String> getSecretQuestions() {
		return secretQuestions;
	}

	public void setSecretQuestions(HashMap<String, String> secretQuestions) {
		this.secretQuestions = secretQuestions;
	}
}
